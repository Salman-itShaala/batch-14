// Increment(++) and Decrement(--) operators

let num = 10;

// num = num + 10;

console.log(num--); // 10

console.log(num); // 9

console.log(++num); // 10

console.log(num); // 10

// Compund assignment operators

let sum = 10;

sum += 10; // sum = sum + 10;

console.log(sum); // 20

sum -= 15; // sum = sum - 15;

console.log(sum); // 5

sum **= 2; // sum = sum ** 2; --> sum = 5 ** 2;

console.log(sum);

